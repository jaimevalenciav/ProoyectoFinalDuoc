-- ─── VISTA: última posición GPS por vehículo ────────────────
CREATE OR REPLACE VIEW GPS_POSICIONES_ACTUALES AS
SELECT *
FROM (
    SELECT
        g.ID, g.EMPRESA_ID, g.VEHICULO_ID, g.CONDUCTOR_ID,
        g.LATITUD, g.LONGITUD, g.VELOCIDAD, g.RUMBO,
        g.ESTADO_MOTOR, g.ODOMETRO, g.RECORDED_AT,
        v.PATENTE, v.MARCA, v.MODELO, v.ESTADO AS ESTADO_VEHICULO,
        c.NOMBRE AS CONDUCTOR_NOMBRE,
        ROW_NUMBER() OVER (PARTITION BY g.VEHICULO_ID ORDER BY g.RECORDED_AT DESC) AS RN
    FROM GPS_TRACKS g
    JOIN VEHICULOS v ON v.ID = g.VEHICULO_ID
    LEFT JOIN CONDUCTORES c ON c.ID = g.CONDUCTOR_ID
)
WHERE RN = 1;

-- ─── VISTA: stock kardex con valorización ───────────────────
CREATE OR REPLACE VIEW KARDEX_REPUESTOS AS
SELECT
    m.ID,
    m.EMPRESA_ID,
    m.REPUESTO_ID,
    r.CODIGO,
    r.DESCRIPCION,
    r.CATEGORIA,
    m.TIPO,
    m.CANTIDAD,
    m.PRECIO_UNIT,
    m.STOCK_ANTERIOR,
    m.STOCK_NUEVO,
    m.COSTO_TOTAL,
    m.REFERENCIA,
    m.DOCUMENTO,
    m.CREATED_AT
FROM MOVIMIENTOS_STOCK m
JOIN REPUESTOS r ON r.ID = m.REPUESTO_ID
ORDER BY m.CREATED_AT DESC;

