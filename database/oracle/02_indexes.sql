-- Trigger: actualizar stock al insertar movimiento
CREATE OR REPLACE TRIGGER TRG_MOV_STOCK_INSERT
    AFTER INSERT ON MOVIMIENTOS_STOCK
    FOR EACH ROW
BEGIN
    IF :NEW.TIPO = 'ENTRADA' THEN
        UPDATE REPUESTOS SET STOCK_ACTUAL = STOCK_ACTUAL + :NEW.CANTIDAD WHERE ID = :NEW.REPUESTO_ID;
    ELSIF :NEW.TIPO = 'SALIDA' THEN
        UPDATE REPUESTOS SET STOCK_ACTUAL = GREATEST(0, STOCK_ACTUAL - :NEW.CANTIDAD) WHERE ID = :NEW.REPUESTO_ID;
    ELSIF :NEW.TIPO = 'AJUSTE' THEN
        UPDATE REPUESTOS SET STOCK_ACTUAL = :NEW.CANTIDAD WHERE ID = :NEW.REPUESTO_ID;
    END IF;
END;
/

-- Trigger: calcular consumo_100km al insertar carga combustible
CREATE OR REPLACE TRIGGER TRG_CARGA_COMBUSTIBLE
    BEFORE INSERT ON CARGAS_COMBUSTIBLE
    FOR EACH ROW
DECLARE
    V_KM_ANT NUMBER;
    V_KM_DIFF NUMBER;
BEGIN
    SELECT MAX(KM_VEHICULO) INTO V_KM_ANT
    FROM CARGAS_COMBUSTIBLE
    WHERE VEHICULO_ID = :NEW.VEHICULO_ID AND KM_VEHICULO < :NEW.KM_VEHICULO;

    IF V_KM_ANT IS NOT NULL THEN
        V_KM_DIFF := :NEW.KM_VEHICULO - V_KM_ANT;
        IF V_KM_DIFF > 0 THEN
            :NEW.CONSUMO_100KM := ROUND((:NEW.LITROS / V_KM_DIFF) * 100, 2);
        END IF;
    END IF;
END;
/
