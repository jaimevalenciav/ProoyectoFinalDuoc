-- ─── ALERTAS ────────────────────────────────────────────────
CREATE TABLE ALERTAS (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    VEHICULO_ID     VARCHAR2(36),
    CONDUCTOR_ID    VARCHAR2(36),
    TIPO            VARCHAR2(30)    NOT NULL,
    SEVERIDAD       VARCHAR2(10)    NOT NULL,
    MENSAJE         VARCHAR2(500)   NOT NULL,
    RESUELTA        NUMBER(1)       DEFAULT 0 NOT NULL,
    RESUELTA_POR    VARCHAR2(36),
    RESUELTA_AT     TIMESTAMP,
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_ALR_EMPRESA   FOREIGN KEY (EMPRESA_ID)  REFERENCES EMPRESAS(ID),
    CONSTRAINT FK_ALR_VEHICULO  FOREIGN KEY (VEHICULO_ID) REFERENCES VEHICULOS(ID),
    CONSTRAINT FK_ALR_CONDUCTOR FOREIGN KEY (CONDUCTOR_ID) REFERENCES CONDUCTORES(ID),
    CONSTRAINT CHK_ALR_TIPO CHECK (TIPO IN ('VELOCIDAD','GEOCERCA','MANTENCION','STOCK','RALENTI','LICENCIA','COMBUSTIBLE')),
    CONSTRAINT CHK_ALR_SEV CHECK (SEVERIDAD IN ('INFO','WARNING','CRITICAL')),
    CONSTRAINT CHK_ALR_RESUELTA CHECK (RESUELTA IN (0,1))
);

-- ─── ÓRDENES DE TRABAJO ─────────────────────────────────────
CREATE TABLE ORDENES_TRABAJO (
    ID                      VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID              VARCHAR2(36)    NOT NULL,
    NUMERO                  VARCHAR2(20)    NOT NULL,
    VEHICULO_ID             VARCHAR2(36)    NOT NULL,
    TIPO                    VARCHAR2(20)    NOT NULL,
    ESTADO                  VARCHAR2(20)    DEFAULT 'PENDIENTE' NOT NULL,
    DESCRIPCION             VARCHAR2(1000)  NOT NULL,
    MECANICO_RESPONSABLE    VARCHAR2(200),
    AVANCE                  NUMBER(3)       DEFAULT 0,
    COSTO_MANO_OBRA         NUMBER(14,2)    DEFAULT 0,
    COSTO_REPUESTOS         NUMBER(14,2)    DEFAULT 0,
    COSTO_TOTAL             NUMBER(14,2)    DEFAULT 0,
    FECHA_APERTURA          DATE            DEFAULT SYSDATE NOT NULL,
    FECHA_CIERRE_EST        DATE,
    FECHA_CIERRE_REAL       DATE,
    NOTAS                   VARCHAR2(2000),
    ELIMINADO               NUMBER(1)       DEFAULT 0 NOT NULL,
    CREATED_AT              TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT              TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_OT_EMPRESA  FOREIGN KEY (EMPRESA_ID)  REFERENCES EMPRESAS(ID),
    CONSTRAINT FK_OT_VEHICULO FOREIGN KEY (VEHICULO_ID) REFERENCES VEHICULOS(ID),
    CONSTRAINT UQ_OT_NUMERO_EMP UNIQUE (EMPRESA_ID, NUMERO),
    CONSTRAINT CHK_OT_TIPO CHECK (TIPO IN ('PREVENTIVA','CORRECTIVA','NEUMATICOS','ELECTRICA')),
    CONSTRAINT CHK_OT_ESTADO CHECK (ESTADO IN ('PENDIENTE','EN_EJECUCION','BLOQUEADA','CERRADA')),
    CONSTRAINT CHK_OT_AVANCE CHECK (AVANCE BETWEEN 0 AND 100),
    CONSTRAINT CHK_OT_ELIMINADO CHECK (ELIMINADO IN (0,1))
);

-- ─── REPUESTOS ──────────────────────────────────────────────
CREATE TABLE REPUESTOS (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    CODIGO          VARCHAR2(50)    NOT NULL,
    DESCRIPCION     VARCHAR2(300)   NOT NULL,
    CATEGORIA       VARCHAR2(100)   NOT NULL,
    UNIDAD          VARCHAR2(20)    DEFAULT 'UN' NOT NULL,
    STOCK_ACTUAL    NUMBER(10)      DEFAULT 0 NOT NULL,
    STOCK_MINIMO    NUMBER(10)      DEFAULT 1 NOT NULL,
    PRECIO_UNITARIO NUMBER(14,2)    DEFAULT 0 NOT NULL,
    PROVEEDOR       VARCHAR2(200),
    ELIMINADO       NUMBER(1)       DEFAULT 0 NOT NULL,
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_REP_EMPRESA FOREIGN KEY (EMPRESA_ID) REFERENCES EMPRESAS(ID),
    CONSTRAINT UQ_REP_CODIGO_EMP UNIQUE (EMPRESA_ID, CODIGO),
    CONSTRAINT CHK_REP_STOCK CHECK (STOCK_ACTUAL >= 0),
    CONSTRAINT CHK_REP_ELIMINADO CHECK (ELIMINADO IN (0,1))
);

-- ─── DETALLE DE OT (repuestos usados) ───────────────────────
CREATE TABLE DETALLE_OT (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    OT_ID           VARCHAR2(36)    NOT NULL,
    REPUESTO_ID     VARCHAR2(36)    NOT NULL,
    CANTIDAD        NUMBER(10)      NOT NULL,
    PRECIO_UNIT     NUMBER(14,2)    NOT NULL,
    SUBTOTAL        NUMBER(14,2)    NOT NULL,
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_DOT_OT       FOREIGN KEY (OT_ID)       REFERENCES ORDENES_TRABAJO(ID),
    CONSTRAINT FK_DOT_REPUESTO FOREIGN KEY (REPUESTO_ID) REFERENCES REPUESTOS(ID)
);

-- ─── MOVIMIENTOS DE STOCK ───────────────────────────────────
CREATE TABLE MOVIMIENTOS_STOCK (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    REPUESTO_ID     VARCHAR2(36)    NOT NULL,
    TIPO            VARCHAR2(20)    NOT NULL,
    CANTIDAD        NUMBER(10)      NOT NULL,
    PRECIO_UNIT     NUMBER(14,2),
    STOCK_ANTERIOR  NUMBER(10)      NOT NULL,
    STOCK_NUEVO     NUMBER(10)      NOT NULL,
    COSTO_TOTAL     NUMBER(14,2),
    REFERENCIA      VARCHAR2(100),
    DOCUMENTO       VARCHAR2(100),
    OT_ID           VARCHAR2(36),
    USUARIO_ID      VARCHAR2(36),
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_MOV_EMPRESA  FOREIGN KEY (EMPRESA_ID)  REFERENCES EMPRESAS(ID),
    CONSTRAINT FK_MOV_REPUESTO FOREIGN KEY (REPUESTO_ID) REFERENCES REPUESTOS(ID),
    CONSTRAINT FK_MOV_OT       FOREIGN KEY (OT_ID)       REFERENCES ORDENES_TRABAJO(ID),
    CONSTRAINT FK_MOV_USUARIO  FOREIGN KEY (USUARIO_ID)  REFERENCES USUARIOS_SISTEMA(ID),
    CONSTRAINT CHK_MOV_TIPO CHECK (TIPO IN ('ENTRADA','SALIDA','AJUSTE'))
);

-- ─── CARGAS DE COMBUSTIBLE ──────────────────────────────────
CREATE TABLE CARGAS_COMBUSTIBLE (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    VEHICULO_ID     VARCHAR2(36)    NOT NULL,
    CONDUCTOR_ID    VARCHAR2(36),
    NUM_DOCUMENTO   VARCHAR2(50),
    PROVEEDOR       VARCHAR2(100)   NOT NULL,
    ESTACION        VARCHAR2(200),
    FECHA_CARGA     DATE            DEFAULT SYSDATE NOT NULL,
    LITROS          NUMBER(10,2)    NOT NULL,
    PRECIO_LITRO    NUMBER(10,2)    NOT NULL,
    COSTO_TOTAL     NUMBER(14,2)    NOT NULL,
    KM_VEHICULO     NUMBER(12)      NOT NULL,
    CONSUMO_100KM   NUMBER(6,2),
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_CC_EMPRESA   FOREIGN KEY (EMPRESA_ID)  REFERENCES EMPRESAS(ID),
    CONSTRAINT FK_CC_VEHICULO  FOREIGN KEY (VEHICULO_ID) REFERENCES VEHICULOS(ID),
    CONSTRAINT FK_CC_CONDUCTOR FOREIGN KEY (CONDUCTOR_ID) REFERENCES CONDUCTORES(ID),
    CONSTRAINT CHK_CC_LITROS CHECK (LITROS > 0)
);

-- ─── SERVICIOS DE TRANSPORTE ────────────────────────────────
CREATE TABLE SERVICIOS (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    CLIENTE_ID      VARCHAR2(36)    NOT NULL,
    VEHICULO_ID     VARCHAR2(36),
    CONDUCTOR_ID    VARCHAR2(36),
    NUM_SERVICIO    VARCHAR2(20)    NOT NULL,
    ORIGEN          VARCHAR2(300)   NOT NULL,
    DESTINO         VARCHAR2(300)   NOT NULL,
    KMS_RECORRIDO   NUMBER(10,2),
    FECHA_SERVICIO  DATE            DEFAULT SYSDATE NOT NULL,
    ESTADO          VARCHAR2(20)    DEFAULT 'PENDIENTE' NOT NULL,
    VALOR_NETO      NUMBER(14,2)    NOT NULL,
    IVA             NUMBER(14,2)    NOT NULL,
    VALOR_TOTAL     NUMBER(14,2)    NOT NULL,
    TIPO_DOCUMENTO  VARCHAR2(30)    DEFAULT 'FACTURA',
    NUM_DOCUMENTO   VARCHAR2(50),
    FECHA_FACTURA   DATE,
    FACTURADO       NUMBER(1)       DEFAULT 0 NOT NULL,
    NOTAS           VARCHAR2(1000),
    ELIMINADO       NUMBER(1)       DEFAULT 0 NOT NULL,
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_SRV_EMPRESA   FOREIGN KEY (EMPRESA_ID)  REFERENCES EMPRESAS(ID),
    CONSTRAINT FK_SRV_CLIENTE   FOREIGN KEY (CLIENTE_ID)  REFERENCES CLIENTES(ID),
    CONSTRAINT FK_SRV_VEHICULO  FOREIGN KEY (VEHICULO_ID) REFERENCES VEHICULOS(ID),
    CONSTRAINT FK_SRV_CONDUCTOR FOREIGN KEY (CONDUCTOR_ID) REFERENCES CONDUCTORES(ID),
    CONSTRAINT UQ_SRV_NUMERO_EMP UNIQUE (EMPRESA_ID, NUM_SERVICIO),
    CONSTRAINT CHK_SRV_ESTADO CHECK (ESTADO IN ('PENDIENTE','EN_CURSO','COMPLETADO','CANCELADO')),
    CONSTRAINT CHK_SRV_TDOC CHECK (TIPO_DOCUMENTO IN ('FACTURA','BOLETA','GUIA_DESPACHO')),
    CONSTRAINT CHK_SRV_FACTURADO CHECK (FACTURADO IN (0,1)),
    CONSTRAINT CHK_SRV_ELIMINADO CHECK (ELIMINADO IN (0,1))
);
