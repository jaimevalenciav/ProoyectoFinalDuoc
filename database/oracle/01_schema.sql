-- ─── EMPRESAS ───────────────────────────────────────────────
CREATE TABLE EMPRESAS (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    NOMBRE          VARCHAR2(200)   NOT NULL,
    RUT             VARCHAR2(20)    NOT NULL UNIQUE,
    GIRO            VARCHAR2(200),
    DIRECCION       VARCHAR2(300),
    CIUDAD          VARCHAR2(100),
    TELEFONO        VARCHAR2(20),
    EMAIL           VARCHAR2(150),
    PLAN            VARCHAR2(20)    DEFAULT 'STANDARD' NOT NULL,
    ACTIVA          NUMBER(1)       DEFAULT 1 NOT NULL,
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CHK_EMPRESA_PLAN CHECK (PLAN IN ('STARTER','STANDARD','PREMIUM')),
    CONSTRAINT CHK_EMPRESA_ACTIVA CHECK (ACTIVA IN (0,1))
);

-- ─── USUARIOS DEL SISTEMA ───────────────────────────────────
CREATE TABLE USUARIOS_SISTEMA (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    AZURE_OID       VARCHAR2(36)    UNIQUE,          -- Object ID de Azure B2C
    NOMBRE          VARCHAR2(200)   NOT NULL,
    EMAIL           VARCHAR2(150)   NOT NULL,
    ROL             VARCHAR2(30)    NOT NULL,
    ACTIVO          NUMBER(1)       DEFAULT 1 NOT NULL,
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_USR_EMPRESA FOREIGN KEY (EMPRESA_ID) REFERENCES EMPRESAS(ID),
    CONSTRAINT CHK_USR_ROL CHECK (ROL IN ('ADMIN','SUPERVISOR','JEFE_TALLER','OPERADOR','CONDUCTOR','BODEGA')),
    CONSTRAINT CHK_USR_ACTIVO CHECK (ACTIVO IN (0,1)),
    CONSTRAINT UQ_USR_EMAIL_EMP UNIQUE (EMPRESA_ID, EMAIL)
);

-- ─── CLIENTES ───────────────────────────────────────────────
CREATE TABLE CLIENTES (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    RUT             VARCHAR2(20)    NOT NULL,
    RAZON_SOCIAL    VARCHAR2(200)   NOT NULL,
    GIRO            VARCHAR2(200),
    DIRECCION       VARCHAR2(300),
    CIUDAD          VARCHAR2(100),
    PAIS            VARCHAR2(100)   DEFAULT 'Chile',
    TELEFONO        VARCHAR2(20),
    EMAIL           VARCHAR2(150),
    REP_LEGAL_NOMBRE VARCHAR2(200),
    REP_LEGAL_RUT   VARCHAR2(20),
    ACTIVO          NUMBER(1)       DEFAULT 1 NOT NULL,
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_CLI_EMPRESA FOREIGN KEY (EMPRESA_ID) REFERENCES EMPRESAS(ID),
    CONSTRAINT UQ_CLI_RUT_EMP UNIQUE (EMPRESA_ID, RUT),
    CONSTRAINT CHK_CLI_ACTIVO CHECK (ACTIVO IN (0,1))
);

-- ─── VEHÍCULOS ──────────────────────────────────────────────
CREATE TABLE VEHICULOS (
    ID                      VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID              VARCHAR2(36)    NOT NULL,
    PATENTE                 VARCHAR2(10)    NOT NULL,
    MARCA                   VARCHAR2(100)   NOT NULL,
    MODELO                  VARCHAR2(100)   NOT NULL,
    ANIO                    NUMBER(4)       NOT NULL,
    TIPO                    VARCHAR2(30)    NOT NULL,
    COMBUSTIBLE             VARCHAR2(20)    DEFAULT 'DIESEL' NOT NULL,
    ESTADO                  VARCHAR2(30)    DEFAULT 'OPERATIVO' NOT NULL,
    KM_ACTUALES             NUMBER(12)      DEFAULT 0 NOT NULL,
    KM_PROXIMO_SERVICIO     NUMBER(12)      DEFAULT 0 NOT NULL,
    VENCIMIENTO_REVISION    DATE,
    VENCIMIENTO_PERMISO     DATE,
    COLOR                   VARCHAR2(50),
    NUM_MOTOR               VARCHAR2(50),
    NUM_CHASIS              VARCHAR2(50),
    QR_CODE                 VARCHAR2(100)   UNIQUE,   -- código QR del camión
    CAPACIDAD_ESTANQUE      NUMBER(6),                -- litros de combustible
    TARA_KG                 NUMBER(8),                -- peso tara en kg
    CAPACIDAD_CARGA_KG      NUMBER(8),                -- capacidad de carga en kg
    ELIMINADO               NUMBER(1)       DEFAULT 0 NOT NULL,
    CREATED_AT              TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT              TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_VEH_EMPRESA FOREIGN KEY (EMPRESA_ID) REFERENCES EMPRESAS(ID),
    CONSTRAINT UQ_VEH_PATENTE_EMP UNIQUE (EMPRESA_ID, PATENTE),
    CONSTRAINT CHK_VEH_TIPO CHECK (TIPO IN ('CAMION','BUS','FURGON','CAMIONETA','OTRO')),
    CONSTRAINT CHK_VEH_COMBUSTIBLE CHECK (COMBUSTIBLE IN ('DIESEL','GASOLINA','GAS','ELECTRICO')),
    CONSTRAINT CHK_VEH_ESTADO CHECK (ESTADO IN ('OPERATIVO','EN_TALLER','FUERA_SERVICIO')),
    CONSTRAINT CHK_VEH_ELIMINADO CHECK (ELIMINADO IN (0,1))
);

-- ─── CONDUCTORES ────────────────────────────────────────────
CREATE TABLE CONDUCTORES (
    ID                      VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID              VARCHAR2(36)    NOT NULL,
    USUARIO_ID              VARCHAR2(36),               -- vínculo con USUARIOS_SISTEMA
    NOMBRE                  VARCHAR2(200)   NOT NULL,
    RUT                     VARCHAR2(20)    NOT NULL,
    TELEFONO                VARCHAR2(20),
    EMAIL                   VARCHAR2(150),
    CATEGORIA_LICENCIA      VARCHAR2(5)     NOT NULL,
    VENCIMIENTO_LICENCIA    DATE            NOT NULL,
    ESTADO                  VARCHAR2(20)    DEFAULT 'ACTIVO' NOT NULL,
    SCORE_CONDUCCION        NUMBER(3)       DEFAULT 100,
    HORAS_MES               NUMBER(6,1)     DEFAULT 0,
    KM_MES                  NUMBER(10)      DEFAULT 0,
    INFRACCIONES_MES        NUMBER(5)       DEFAULT 0,
    ELIMINADO               NUMBER(1)       DEFAULT 0 NOT NULL,
    CREATED_AT              TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT              TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_COND_EMPRESA FOREIGN KEY (EMPRESA_ID) REFERENCES EMPRESAS(ID),
    CONSTRAINT FK_COND_USUARIO FOREIGN KEY (USUARIO_ID) REFERENCES USUARIOS_SISTEMA(ID),
    CONSTRAINT UQ_COND_RUT_EMP UNIQUE (EMPRESA_ID, RUT),
    CONSTRAINT CHK_COND_ESTADO CHECK (ESTADO IN ('ACTIVO','INACTIVO','VACACIONES')),
    CONSTRAINT CHK_COND_SCORE CHECK (SCORE_CONDUCCION BETWEEN 0 AND 100),
    CONSTRAINT CHK_COND_ELIMINADO CHECK (ELIMINADO IN (0,1))
);

-- ─── ASIGNACIONES ───────────────────────────────────────────
CREATE TABLE ASIGNACIONES (
    ID              VARCHAR2(36)    DEFAULT SYS_GUID() PRIMARY KEY,
    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    VEHICULO_ID     VARCHAR2(36)    NOT NULL,
    CONDUCTOR_ID    VARCHAR2(36)    NOT NULL,
    FECHA_INICIO    DATE            DEFAULT SYSDATE NOT NULL,
    FECHA_FIN       DATE,
    ACTIVA          NUMBER(1)       DEFAULT 1 NOT NULL,
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_ASIG_EMPRESA   FOREIGN KEY (EMPRESA_ID)  REFERENCES EMPRESAS(ID),
    CONSTRAINT FK_ASIG_VEHICULO  FOREIGN KEY (VEHICULO_ID) REFERENCES VEHICULOS(ID),
    CONSTRAINT FK_ASIG_CONDUCTOR FOREIGN KEY (CONDUCTOR_ID) REFERENCES CONDUCTORES(ID),
    CONSTRAINT CHK_ASIG_ACTIVA CHECK (ACTIVA IN (0,1))
);

-- ─── GPS TRACKS ─────────────────────────────────────────────
CREATE TABLE GPS_TRACKS (
    ID              NUMBER          GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    VEHICULO_ID     VARCHAR2(36)    NOT NULL,
    CONDUCTOR_ID    VARCHAR2(36),
    LATITUD         NUMBER(11,7)    NOT NULL,
    LONGITUD        NUMBER(11,7)    NOT NULL,
    VELOCIDAD       NUMBER(5,1)     DEFAULT 0,
    RUMBO           NUMBER(5,1)     DEFAULT 0,
    PRECISION_M     NUMBER(6,1),
    ESTADO_MOTOR    VARCHAR2(10)    DEFAULT 'ENCENDIDO',
    ODOMETRO        NUMBER(12),
    RECORDED_AT     TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_GPS_EMPRESA   FOREIGN KEY (EMPRESA_ID)  REFERENCES EMPRESAS(ID),
    CONSTRAINT FK_GPS_VEHICULO  FOREIGN KEY (VEHICULO_ID) REFERENCES VEHICULOS(ID),
    CONSTRAINT FK_GPS_CONDUCTOR FOREIGN KEY (CONDUCTOR_ID) REFERENCES CONDUCTORES(ID),
    CONSTRAINT CHK_GPS_MOTOR CHECK (ESTADO_MOTOR IN ('ENCENDIDO','APAGADO'))
);

-- ─── GEOCERCAS ──────────────────────────────────────────────
CREATE TABLE GEOCERCAS (
