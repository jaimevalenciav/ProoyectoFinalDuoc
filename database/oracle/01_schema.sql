    EMPRESA_ID      VARCHAR2(36)    NOT NULL,
    NOMBRE          VARCHAR2(100)   NOT NULL,
    TIPO            VARCHAR2(20)    DEFAULT 'CIRCULO',
    LATITUD_CENTRO  NUMBER(11,7),
